public class relational_op {
    public static void main(String[] args) {
        //relational operator
        int a,b;
        a=10;
        b=20;
        System.out.println(a==b);
        
        System.out.println(a!=b);

        System.out.println(a>b);

        System.out.println(a<b);

        System.out.println(a>=b);

        System.out.println(a<=b);

    }
    
}
